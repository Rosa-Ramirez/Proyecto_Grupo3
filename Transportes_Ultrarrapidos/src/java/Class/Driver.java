public class Driver {
    private int dpi;
    private String name;
    private String lastName;
    private int phone;
    private String address;

    public Driver(int dpi, String name, String lastName, int phone, String address) {
        this.dpi = dpi;
        this.name = name;
        this.lastName = lastName;
        this.phone = phone;
        this.address = address;
    }

    public int getDpi() {
        return dpi;
    }

    public void setDpi(int dpi) {
        this.dpi = dpi;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public int getPhone() {
        return phone;
    }

    public void setPhone(int phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
    
}
